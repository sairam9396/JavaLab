public class Animal{
	public void eat(){
		System.out.println("Animal is Eating");
	}
	public void sleep(){
		System.out.println("Animal is Sleeping");
	}
	public void roam(){
		System.out.println("Animal is Roaming");
	}
}

class Feline extends Animal{
	public void eat(){
		System.out.println("Feline animal is Eating");
	}
	public void sleep(){
		System.out.println("Feline animal is Sleeping");
	}
	public void roam(){
		System.out.println("Feline animal is Roaming");
	}
}





